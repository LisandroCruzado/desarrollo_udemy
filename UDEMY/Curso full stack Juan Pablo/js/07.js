//Incrementos

puntaje = 10;

// ++ o -- suma o resta 1

console.log(++puntaje); // Suma y da el resultado
console.log(puntaje++); // Da el resultado y suma en el próximo llamado
console.log(puntaje); // Da el resultado

console.log(--puntaje); // Resta y da el resultado
console.log(puntaje--); // Da el resultado y resta en el próximo llamado
console.log(puntaje); // Da el resultado

// += o -= Suma o Resta mas de 1

console.log(puntaje += 20);